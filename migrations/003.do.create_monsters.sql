CREATE TABLE monsters (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    monster_name TEXT NOT NULL,
    monster_type TEXT NOT NULL,
    challenge_rating TEXT NOT NULL,
    proficiencyBonus INTEGER NOT NULL,
    armorClass INTEGER NOT NULL,
    hitPoints INTEGER NOT NULL,
    speed TEXT NOT NULL,
    attackBonus INTEGER NOT NULL,
    saveDc INTEGER NOT NULL,
    strength INTEGER NOT NULL,
    strengthSave BOOLEAN NOT NULL,
    dexterity INTEGER NOT NULL,
    dexteritySave BOOLEAN NOT NULL,
    constitution INTEGER NOT NULL,
    constitutionSave BOOLEAN NOT NULL,
    inteligence INTEGER NOT NULL,
    inteligenceSave BOOLEAN NOT NULL,
    wisdom INTEGER NOT NULL,
    wisdomSave BOOLEAN NOT NULL,
    charisma INTEGER NOT NULL,
    charismaSave BOOLEAN NOT NULL,
    damageVulnerability TEXT,
    damageResistances TEXT,
    damageImmunities TEXT,
    senses TEXT,
    creature_language TEXT,
    notes TEXT,
    user_id INTEGER
        REFERENCES user_table(id) ON DELETE CASCADE NOT NULL
)