CREATE TABLE monster_starting_point (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    combat_rating TEXT NOT NULL,
    proficiencyBonus INTEGER NOT NULL,
    armorClassSuggestion TEXT NOT NULL,
    armorClassStartingPoint INTEGER NOT NULL,
    hitPointsSuggestion TEXT NOT NULL,
    hitPointsMin INTEGER NOT NULL,
    hitPointsMax INTEGER NOT NULL,
    attackBonusSuggestion TEXT NOT NULL,
    attackBonus INTEGER NOT NULL,
    damagePerRoundSuggestion TEXT NOT NULL,
    saveDcSuggestion TEXT NOT NULL,
    saveDc INTEGER NOT NULL,
)